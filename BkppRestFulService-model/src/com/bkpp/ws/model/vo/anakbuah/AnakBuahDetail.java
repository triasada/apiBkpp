package com.bkpp.ws.model.vo.anakbuah;

/**
 *
 * @author mukticahya
 */
public class AnakBuahDetail {
    String namaLengkap;
    String nip;
    String kdOrganisasi;
    String idIdentitas;
    String kdJabatan;
    String parent;

    public String getNamaLengkap() {
        return namaLengkap;
    }

    public void setNamaLengkap(String namaLengkap) {
        this.namaLengkap = namaLengkap;
    }

    public String getNip() {
        return nip;
    }

    public void setNip(String nip) {
        this.nip = nip;
    }

    public String getKdOrganisasi() {
        return kdOrganisasi;
    }

    public void setKdOrganisasi(String kdOrganisasi) {
        this.kdOrganisasi = kdOrganisasi;
    }

    public String getIdIdentitas() {
        return idIdentitas;
    }

    public void setIdIdentitas(String idIdentitas) {
        this.idIdentitas = idIdentitas;
    }

    public String getKdJabatan() {
        return kdJabatan;
    }

    public void setKdJabatan(String kdJabatan) {
        this.kdJabatan = kdJabatan;
    }

    public String getParent() {
        return parent;
    }

    public void setParent(String parent) {
        this.parent = parent;
    }
   
}
